package com.conehanor.kfcserver.model;

public class HanfcStatics {
    int curEmployee;
    int recruitEmployeeCurMonth;
    int fireEmployeeCurMonth;

    int orderCount;
    int suggestionCount;
    int customerCount;


    public int getCurEmployee() {
        return curEmployee;
    }

    public void setCurEmployee(int curEmployee) {
        this.curEmployee = curEmployee;
    }

    public int getRecruitEmployeeCurMonth() {
        return recruitEmployeeCurMonth;
    }

    public void setRecruitEmployeeCurMonth(int recruitEmployeeCurMonth) {
        this.recruitEmployeeCurMonth = recruitEmployeeCurMonth;
    }

    public int getFireEmployeeCurMonth() {
        return fireEmployeeCurMonth;
    }

    public void setFireEmployeeCurMonth(int fireEmployeeCurMonth) {
        this.fireEmployeeCurMonth = fireEmployeeCurMonth;
    }

    public int getOrderCount() {
        return orderCount;
    }

    public void setOrderCount(int orderCount) {
        this.orderCount = orderCount;
    }

    public int getSuggestionCount() {
        return suggestionCount;
    }

    public void setSuggestionCount(int suggestionCount) {
        this.suggestionCount = suggestionCount;
    }

    public int getCustomerCount() {
        return customerCount;
    }

    public void setCustomerCount(int customerCount) {
        this.customerCount = customerCount;
    }
}
